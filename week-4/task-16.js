function graduates (students) {
    // Code disini
    var final={};
    if(students.length<1){
        return final;
    } else{
        for(var i=0; i<students.length; i++){
            if(!(students[i].class in final)){
                final[students[i].class]=[];
            }
        }

        for(var keys in final){
            for(var i=0; i<students.length; i++){
                var temp={};
                if(keys===students[i].class && students[i].score>75){
                    temp.name=students[i].name;
                    temp.score=students[i].score;
                    final[keys].push(temp)
                }
            }
        }

        return final;
    }
  }
  
